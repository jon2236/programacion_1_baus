from math import pi


def calcular_perimetro_circunferencia(radio: int) -> float:
    from math import pi
    return 2 * radio * pi



def calcular_perimetro_rectangulo(base:int, altura:int) -> int:
    return 2 * (base + altura)



def calcular_perimetro_cuadrado(lado:int) -> int:
    return 4 * (lado)
